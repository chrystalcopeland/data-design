<h1>
	Use Case
</h1>
<p>
	Cara is planning 3 summer trips with her dog, Buddy. She wants to visit pet friendly national parks, eat at restauraunts
	with patios so she can take her dog, and stay in pet friendly hotels.
</p>
<h2>
	Preconditions
</h2>
	<ul>
		<li>
			User has an account and is logged in.
		</li>
		<li>
			User is able to search pre-populated data for pet friendly destinations, restaurants and hotels
		</li>
	</ul>
<h2>
	Postconditions
</h2>
	<ul>User can track destinations to plan a trip with her pet</ul>
<h1>
	Interaction Flow
</h1>
<h2>
	Steps Cara must take to plan a trip
</h2>
<ol>
	<li>
		navigates to website and enters a city destination in a search bar
	</li>
	<li>
		search returns a list of pre-populated pet friendly places and things to do at the destination.
	</li>
	<li>
		Cara is able to save her favorite destinations to plan her trip by clicking on a star icon
	</li>
	<li>
		The star is lit yellow to indicate Cara wants to visit this place and it is saved to her profile.
	</li>
	<li>
		Cara can view her saved places under her profile
	</li>
</ol>
